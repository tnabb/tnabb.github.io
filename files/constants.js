function pc_calc_weapontype(sd) {
    if(sd.weapontype2 == WEAPON.FIST) {
        sd.status.weapon = sd.weapontype1;
        return;
    }
    if(sd.weapontype1 == WEAPON.FIST) {
        sd.status.weapon = sd.weapontype2;
        return;
    }
    // dual wield
    sd.status.weapon = WEAPON.FIST;
    switch(sd.weapontype1) {
        case WEAPON.DAGGER:
            switch(sd.weapontype2) {
                case WEAPON.DAGGER: sd.status.weapon = WEAPON.DOUBLE_DD; break;
                case WEAPON.ONEHANDSWORD: sd.status.weapon = WEAPON.DOUBLE_DS; break;
                case WEAPON.ONEHANDAXE: sd.status.weapon = WEAPON.DOUBLE_DA; break;
            }
            break;
        case WEAPON.ONEHANDSWORD:
            switch(sd.weapontype2) {
                case WEAPON.DAGGER: sd.status.weapon = WEAPON.DOUBLE_DS; break;
                case WEAPON.ONEHANDSWORD: sd.status.weapon = WEAPON.DOUBLE_SS; break;
                case WEAPON.ONEHANDAXE: sd.status.weapon = WEAPON.DOUBLE_SA; break;
            }
            break;
        case WEAPON.ONEHANDAXE:
            switch(sd.weapontype2) {
                case WEAPON.DAGGER: sd.status.weapon = WEAPON.DOUBLE_DA; break;
                case WEAPON.ONEHANDSWORD: sd.status.weapon = WEAPON.DOUBLE_SA; break;
                case WEAPON.ONEHANDAXE: sd.status.weapon = WEAPON.DOUBLE_AA; break;
            }
            break;
    }
    if(!sd.status.weapon)
        sd.status.weapon = sd.weapontype1;
}